import math

# Данные задачи
h = 0.010  # Высота в метрах
sigma_h = 0.00050  # Погрешность высоты в метрах
S = 40.00  # Расстояние в метрах
sigma_S = 0.010  # Погрешность расстояния в метрах
n = 10  # Количество делений
sigma_n = 0.10  # Погрешность количества делений

# Постоянная величина
constant = 206265

# Частные производные
dt_dh = constant / (n * S)
dt_dS = -constant * h / (n * S**2)
dt_dn = -constant * h / (n**2 * S)

# Квадратическая сумма погрешностей
sigma_t = math.sqrt(
    (dt_dh * sigma_h)**2 +
    (dt_dS * sigma_S)**2 +
    (dt_dn * sigma_n)**2
)

# Вывод результата
print(f"Средняя квадратическая погрешность: {sigma_t:.4f} с")
