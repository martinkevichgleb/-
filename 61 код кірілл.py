import math

# Данные задачи
m1 = 3.0  # Погрешность первого угла (в секундах)
m2 = 4.0  # Погрешность второго угла (в секундах)
m4 = 2.0  # Погрешность четвёртого угла (в секундах)

# Погрешность третьего угла равна погрешности первого
m3 = m1

# Вычисление средней квадратической погрешности суммы
sigma_S = math.sqrt(m1**2 + m2**2 + m3**2 + m4**2)

# Вывод результата
print(f"Средняя квадратическая погрешность суммы углов: {sigma_S:.2f}''")