import math

# Данные задачи
h = 0.5  # Высота сечения (м)
sigma_h = 0.05  # Погрешность высоты (10% от h)
l = 7 / 1000  # Заложение на карте (м)
scale = 2000  # Масштаб карты
sigma_l = 0.1 / 1000  # Погрешность заложения на карте (м)

# Вычисляем реальное заложение и его погрешность
L = l * scale  # Реальное заложение (м)
sigma_L = sigma_l * scale  # Погрешность реального заложения (м)

# Производные
di_dh = 1 / L
di_dL = -h / (L**2)

# Вычисляем среднюю квадратическую погрешность
sigma_i = math.sqrt(
    (di_dh * sigma_h)**2 +
    (di_dL * sigma_L)**2
)

# Вывод результата
print(f"Средняя квадратическая погрешность крутизны ската: {sigma_i:.4f}")